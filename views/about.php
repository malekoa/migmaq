        <p>This website is designed to help you learn Mi'gmaq. You may already know some of the language, but want a chance to practice and improve your knowledge. Or you may have no exposure to the language at all. Because this website is self-guided, you can move through the material at the pace that is comfortable to you.</p>

        <p>Listening and speaking are crucial language skills. While we include some written exercises, this site is primarily meant to help you speak and understand Mi'gmaq, and each lesson includes recordings of Mi'gmaq speakers for you to listen to. Every time you listen to a recording, you should also practice saying it out loud. Gaining confidence in speaking is an important part of learning the language.</p>

        <p>These lessons were developed from class notes taken during Mary Ann Metallic's language classes at the Listuguj Education Directorate with Mary Ann Metallic. These notes were formulated into over one hundred lessons. Joe Wilmot designed and recorded audio files to accompany the lessons, with the help of many Mi'gmaq speakers.</p>

        <p>The material is arranged into sections, units, and lessons. Sections are divided thematically based on typical topics of conversation. Sections become increasingly advanced as more complex grammar is introduced. Units typically contain five to seven lessons, each focusing on a subtopic related to the section's theme. The final unit in each section is a review of previous material.</p>

        <p>A lesson is the smallest amount of material a user should study at one time. Lessons introduce new vocabulary, provide recorded dialogs to help you practice your listening and speaking skills, and test your knowledge with simple exercises.</p>
